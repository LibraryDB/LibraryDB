package model;

public class BorrowingFrequency {
	
	private String b;
	private int f;

	public BorrowingFrequency(String b, int f) {
		this.b = b;
		this.f = f;
	}

	public String getB() {
		return b;
	}

	public void setB(String b) {
		this.b = b;
	}

	public int getF() {
		return f;
	}

	public void setF(int f) {
		this.f = f;
	}
	
	

}
